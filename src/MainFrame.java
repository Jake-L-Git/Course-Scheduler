    private String determineStatus(String semester, String courseCode) {
        String status = "";
        int seats = ClassQueries.getClassSeats(semester, courseCode);
        int enrolled = ScheduleQueries.getScheduledStudentCount(semester, courseCode);

        if (enrolled >= seats) {
            status = "waitlisted";
        } else {
            status = "scheduled";
        }

        return status;
    }


