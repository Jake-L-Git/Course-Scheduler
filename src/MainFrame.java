    private void adminDropStudentSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_adminDropStudentSubmitActionPerformed
        // TODO add your handling code here:
        String displayText = "";
        String studentNameID = adminStudentDropdown.getSelectedItem().toString();
        String studentID = studentNameID.substring(studentNameID.lastIndexOf(" ") + 1);

        ArrayList<String> semesters = SemesterQueries.getSemesterList();

        String studentNameId = StudentQueries.getStudent(studentID).toString();
        displayText = studentNameId + " has been dropped from the list of students\n";
        StudentQueries.dropStudent(studentID);

        for (int i = 0; i < semesters.size(); i++) {
            String semester = semesters.get(i);
            displayText += "\nFor semester: " + semester + "\n";

            ArrayList<ScheduleEntry> entry = ScheduleQueries.getScheduleByStudent(semester, studentID);

            for (ScheduleEntry scheduleEntry : entry) {
               // displayText += scheduleEntry.getCourseCode() + "\t" + scheduleEntry.getStatus() + "\t" + studentID + "\t" + semester + "\n";

                if (scheduleEntry.getStatus().equals("scheduled")) {
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, scheduleEntry.getCourseCode());
                    displayText += studentNameId + " has been dropped from " + CourseQueries.getCourse(scheduleEntry.getCourseCode()).getDescription() + "\n";

                    ArrayList<ScheduleEntry> waitlistEntry = ScheduleQueries.getWaitlistedStudentsByClass(semester, scheduleEntry.getCourseCode());

                    if (waitlistEntry.size() > 0) {

                      //  displayText += waitlistEntry.get(0).getStudentID() + waitlistEntry.get(0).getStudentID() + " is in waitlist for " + waitlistEntry.get(0).getCourseCode() + "\n";
                        displayText += StudentQueries.getStudent(waitlistEntry.get(0).getStudentID()).toString() + " has been scheduled into " + CourseQueries.getCourse(scheduleEntry.getCourseCode()).getDescription() + "\n";
                        ScheduleQueries.updateScheduleEntry(waitlistEntry.get(0));

                    }

                } else {
                    //can assume its waitlisted
                    ScheduleQueries.dropStudentScheduleByCourse(semester, studentID, scheduleEntry.getCourseCode());
                    displayText += studentNameId + " has been dropped from the waitlist for " + CourseQueries.getCourse(scheduleEntry.getCourseCode()).getDescription() + "\n";

                }
            }

        }

        jTextArea1.setText(displayText);


    }//GEN-LAST:event_adminDropStudentSubmitActionPerformed

